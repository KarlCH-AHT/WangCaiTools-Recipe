export function t(key: string, language: Language): string {
  return translations[language]?.[key] ?? key;
}

/**
 * Translate a unit string (e.g. "g", "tbsp") into the current language.
 * Falls back to the raw unit string if no translation is found.
 */
export function formatUnit(unit: string, language: Language): string {
  if (!unit) return unit;
  return translations[language]?.[unit] ?? unit;
}
